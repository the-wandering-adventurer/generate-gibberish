#!/usr/bin/env python3
import base64
import gzip
import json
import random
import re
import sys

with open(sys.argv[0]) as f:
    while line := f.readline():
        if line.strip() == "# ___END_OF_SHELL_SCRIPT___":
            break
    data = f.read()
    data = re.sub('"""', "", data)
    model = json.loads(gzip.decompress(base64.b64decode(data.encode())).decode())

states = model["states"]


def gen_word(length):
    if length == 1:
        return random.choice(model["one_char_words"])
    if length == 2:
        return random.choice(model["two_char_words"])
    word = random.choice(model["start_chars"])
    for i in range(0, length - 2):
        key = word[i : i + 2]
        if key in states:
            next_char = random.choices(
                list(states[key].keys()), cum_weights=list(states[key].values())
            )
            next_char = next_char[0]
        else:
            next_char = random.choice(model["one_char_words"])
        word += next_char
    return word


num_words = random.randint(0, 4) + random.randint(0, 4) + 3

sentence = []

for i in range(0, num_words):
    num_chars = random.randint(0, 6) + random.randint(0, 6) + 1
    word = gen_word(num_chars)
    sentence.append(word)

print(" ".join(sentence))

exit(0)
